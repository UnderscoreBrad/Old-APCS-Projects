
public class Contacts {

    private String name, phone;

    public Contacts() {
        name = "";
        phone = "";
    }

    public String getName() {
        return (name);
    }

    public String getPhone() {
        return (phone);
    }

    public void setName(String nm) {
        name = nm;
    }

    public void setPhone(String ph) {
        phone = ph;
    }
}
